    public function sectionIndex()
    {
      $locale = LaravelLocalization::getCurrentLocale();
      $sections = Section::valid()->with(["translations"=>function ($query) use ($locale){
        $query->select("section_id","name")->where("locale",$locale);
      }])->select('id')->get();
      return response()->json($sections);

